public class GroupedValues {
    public String A;
    public String R;
    public String G;
    public String B;

    GroupedValues()
    {
        A = "255";
        R = "255";
        G = "255";
        B = "255";
    }


    GroupedValues(String a, String r, String g, String b)
    {
        A = a;
        R = r;
        G = g;
        B = b;
    }

}
